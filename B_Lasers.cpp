#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main() {
    int t; cin >> t;
    while(t--) {
        ll n, m, x, y;
        cin >> n >> m >> x >> y;
        
        for(int i = 0; i < n; i++) {
            ll ai; cin >> ai;
        }
        
        for(int i = 0; i < m; i++) {
            ll bi; cin >> bi;
        }

        cout << n+m << '\n';

    } 


}
